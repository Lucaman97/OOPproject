package com.esame.progetto.model;

public class StatisticheNumeri {
	
	//attributi
private int avg;
private int min;
private int max;
private int devStd;
private int sum;
private int count;

	//metodi
public int getAvg() {
	return avg;
}
public void setAvg(int avg) {
	this.avg = avg;
}
public int getMin() {
	return min;
}
public void setMin(int min) {
	this.min = min;
}
public int getMax() {
	return max;
}
public void setMax(int max) {
	this.max = max;
}
public int getDevStd() {
	return devStd;
}
public void setDevStd(int devStd) {
	this.devStd = devStd;
}
public int getSum() {
	return sum;
}
public void setSum(int sum) {
	this.sum = sum;
}
public int getCount() {
	return count;
}
public void setCount(int count) {
	this.count = count;
}


}
